/*2. Происхождение(образование) товара
     Давайте немножко глобального и фундаментального. Ничто не возникает ниоткуда. Яблоки - не возникают ниоткуда. Они создаются из энергии Солнца (абсолютного колоссального источника) и биологических условий Земли. Энергии у Солнца хоть отбавляй, и оно её постоянно тратит практически впустую. Но часть всё же попадает на нашу Землю. 
     Итак, яблоки (вернее яблони) получают энергию напрямую (ну там, через через оксигенный хлорофильный фотосинтез). Человеку же, что бы поддерживать жизнедеятельность, тоже необходима энергия. Но человек, к сожалению,  не может брать энергию от Солнца напрямую. Он получает её от усваивания пищи. Тех же яблок. Т.к. яблоки растут на деревьях, то человеку необходимо затратить часть энергии на их сбор - т.е. совершить труд. 
    При совершении труда человек присоединяет свой труд к товару. Напомним: яблоки, растущие на дереве - еще не являются товаром. Более доходчиво: земляника в лесу - не является товаром. Но если её собрать в баночку - то тогда земляника становится товаром. 
     Вот человек за 1 час собирает 10 кг яблок - он присоединяет к яблокам свой труд и они обретают меновую стоимость. Становятся равны 1 бушелю пшеницы или 1 кг хлопка или 100 рублям. Или 1 часу работы. 
     
     Т.е. один час труда стоит 100 рублей. 
     Труд - это такой же товар. И любой человек может продать этот товар. 
     
     Давайте вспомним, что человек не может питаться пшеницей в её натуральном виде. Он должен приложить к ней труд, чтобы та превратилась в хлеб. 
     В такой цепочке: пшеница -> зерно -> мука -> хлеб
     Допустим, из 1-го бушеля пшеницы получается 10 кг зерна. Равна ли меновая стоимость 1-го бушеля пшеницы и 10 кг зерна? Нет. Т.к. для того, чтобы получить зерно из пшеницы необходимо приложить труд. Допустим, Пшеничник делает 10 кг зерна из бушеля за 2 часа. Его час работы стоит 100 рублей. Следовательно: 
     1 б. пшеницы + 2 часа труда = 10 кг зерна
     или (т.к. 1 б. пшеницы = 1 час труда)
     1 час труда + 2 часа труда = 10 кг зерна = 300 рублей
     
     Труд в своем процессе как-бы налипает на пшеницу, и она превращается в зерно.
     Точно так же труд налипает на железную руду, на песок, деревья, на нефть и проч. и те  в итоге становятся комбайном. Допустим, комбайн рассчитан на 10 лет работы. Комбайн - это сгусток труда. Стоимость этого труда, воплощенного в комбайн, за 10 лет работы налипает на собранную с его помощью пшеницу (ну и плюс труд комбайнера).
 Т.к. такой способ производства пшеницы гораздо эффективней ручного труда, то меновая стоимость 1-го бушеля пшеницы, собранной с помощью комбайна, будет не 100 рублей, а, например, 10 рублей.
    
    Кстати о средствах производства. Зерно люди тоже не едят, и поэтому его требуется перемолоть в муку. Чтобы это сделать, необходим ручной жернов или мельница.
    Допустим, Пшеничник сделал очень плохой жернов за 50 часов ручной работы. Стоимость такого жернова составляет 50 часов труда * 100 рублей = 5000 рублей.
    Допустим, жернов рассчитан на 250 часов работы. А потом приходит в негодность. 
 Значит, 1 час работы жернова составляет: 5000 руб. / 250 час. = 20 рублей.
    Теперь рассчитаем стоимость муки полученной Пшеничником при помощи жернова.
    Из 10 кг зерна пшеничник делает 5 кг муки за 1 час.
  Следовательно - мука стоит: 
   10 кг зерна + 1 час труда + 1 час ресурса жернова 
      =  300 рублей + 100 рублей + 20 рублей = 420 рублей (5 кг муки)
  Опять же, здесь на зерно налипает непосредственный труд Пшеничника, и труд, затраченный на производство жернова.
      3. Формула стоимости товара!
