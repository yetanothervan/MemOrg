  /*1.1. Товар
   Всё, что покупается/продается - является товаром. Например: пшеница, зерно, хлеб, комбайн. Все, что потребляется напрямую человеком - является товаром потребления. Т.е. еда, одежда, предметы быта, автомобили, жилые дома. 
